	t2.name AS away_team_name, att.seed AS away_team_seed,
	home_set1_score, away_set1_score, home_set1_tiebreak, away_set1_tiebreak,
	home_set2_score, away_set2_score, home_set2_tiebreak, away_set2_tiebreak,
